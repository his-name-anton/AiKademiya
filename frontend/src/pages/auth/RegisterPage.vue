const router = useRouter()
const authStore = useAuthStore()
const toast = useToast()

// Form state
const loading = ref(false)
const error = ref('')
const showPassword = ref(false)
const agreedToTerms = ref(false)

const registerData = reactive({
  email: '',
  username: '',
  password1: '',
  password2: ''
})

const fieldErrors = reactive({
  email: '',
  username: '',
  password1: '',
  password2: ''
})

// Methods
const handleRegister = async () => {
  loading.value = true
  error.value = ''
  
  // Clear field errors
  Object.keys(fieldErrors).forEach(key => {
    fieldErrors[key as keyof typeof fieldErrors] = ''
  })

  // Validate passwords match
  if (registerData.password1 !== registerData.password2) {
    fieldErrors.password2 = '–ü–∞—Ä–æ–ª–∏ –Ω–µ —Å–æ–≤–ø–∞–¥–∞—é—Ç'
    loading.value = false
    return
  }

  try {
    await authStore.register({
      email: registerData.email,
      username: registerData.username,
      password: registerData.password1,
      password_confirm: registerData.password2
    })
    
    toast.success('–†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è —É—Å–ø–µ—à–Ω–∞! –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!')
    router.push('/')
  } catch (err: any) {
    if (err.response?.data?.errors) {
      // Handle field-specific errors
      const errors = err.response.data.errors
      fieldErrors.email = errors.email?.[0] || ''
      fieldErrors.username = errors.username?.[0] || ''
      fieldErrors.password1 = errors.password?.[0] || errors.password1?.[0] || ''
      fieldErrors.password2 = errors.password_confirm?.[0] || errors.password2?.[0] || ''
    } else {
      error.value = err.response?.data?.message || '–û—à–∏–±–∫–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.'
    }
  } finally {
    loading.value = false
  }
}

